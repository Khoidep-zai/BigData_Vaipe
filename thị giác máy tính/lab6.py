def find_common_misclassifications(model1, model2, loader):
    model1.eval()
    model2.eval()
    common_errors = []

    with torch.no_grad():
        for images, labels in loader:
            images_dev = images.to(device)
            out1 = model1(images_dev)
            out2 = model2(images_dev)
            _, pred1 = torch.max(out1, 1)
            _, pred2 = torch.max(out2, 1)

            pred1 = pred1.cpu()
            pred2 = pred2.cpu()

            # Tìm các vị trí mà cả 2 model đều sai so với labels
            mask = (pred1 != labels) & (pred2 != labels)
            if mask.any():
                indices = torch.where(mask)[0]
                for idx in indices:
                    common_errors.append({
                        'image': images[idx],
                        'true': labels[idx].item(),
                        'pred1': pred1[idx].item(),
                        'pred2': pred2[idx].item()
                    })
    return common_errors

